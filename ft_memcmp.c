#include "libft.h"

int	ft_memcmp(const void *s1, const void *s2, size_t n)
{
	unsigned int		i;
	unsigned const char	*f;
	unsigned const char	*s;

	f = s1;
	s = s2;
	i = 0;
	if (!f || !s)
		return (0);
	while (i < n)
	{
		if (f[i] != s[i])
			return (f[i] - s[i]);
		i++;
	}
	return (0);
}
